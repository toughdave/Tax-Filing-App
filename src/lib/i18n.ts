export const translations: Record<Locale, TextDictionary> = { en, fr };

export function resolveLocale(raw: string | string[] | null | undefined): Locale {
  const value = Array.isArray(raw) ? raw[0] : raw;
  return value === "fr" ? "fr" : "en";
}

export function textFor(locale: Locale): TextDictionary {
  return translations[locale];
}

export function withLang(pathname: string, locale: Locale): string {
  const separator = pathname.includes("?") ? "&" : "?";
  return `${pathname}${separator}lang=${locale}`;
}
